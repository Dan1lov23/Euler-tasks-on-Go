// Если выписать все натуральные числа меньше 10, кратные 3 или 5, то получим 3, 5, 6 и 9.
// Сумма этих чисел равна 23. 
// Найдите сумму всех чисел меньше 1000, кратных 3 или 5.

package main
import "fmt"

func task1() int {
	sum := 0
	for a := 1; a < 1000; a++ {
		if a % 3 == 0 || a % 5 == 0 {
			sum += a
		}
	}
	return sum
}

func main() {
	fmt.Println(task1())
}
